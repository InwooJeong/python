# 문자열 연결 : +
print("안녕" + "하세요.")

# 문자열 반복 : *
print("안녕하세요." * 3)
print(3 * "안녕하세요.") 

# 문자열 선택 연산자(인덱싱) : []
print("안녕하세요"[0])
print("안녕하세요"[1])
print("안녕하세요"[2])
print("안녕하세요"[3])
print("안녕하세요"[4])

# []안의 숫자를 음수로 입력하면 뒤에서부터 선택
print("안녕하세요"[-1])
print("안녕하세요"[-2])
print("안녕하세요"[-3])
print("안녕하세요"[-4])
print("안녕하세요"[-5])

# 문자열 범위 선택 연산자(슬라이싱) : [:]
print("안녕하세요"[1:4])
print("안녕하세요"[0:2])
print("안녕하세요"[1:])
print("안녕하세요"[:3])